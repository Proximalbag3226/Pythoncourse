#Funciones integradas para convertir tipos de datos

#Valor de tipo entero
a = int("10")
print(a)

#Valor de tipo flotante
b = float("10.8")
print(b)

#Valor de tipo string 
c = str(10)
print(c)

#De flotante a string 
d =  str(10.98)
print(d)

#De entero a binario 
e = bin(10)
print(e)

#De entero a hexadecimal 
f = hex(10)
print(f)

#De binario a entero 
g = int("0b1010", 2)
print(g)

#De hexadecimal a entero 
h = int("0xa", 16)
print(h)