#Operacodres realacionales

#menor que...
resultado1 = 10 < 20
print(resultado1)

#mayor que...
resultado2 = 10 > 20
print (resultado2)

#igual que...
resultado3 = 10 == 20
print(resultado3)

#diferente de...   
resultado4 = 10 != 20
print(resultado4)

#menor igual que...
resultado5 = 10 <= 20
print(resultado5)

#mayor o igual que...
resultado6 = 10 >= 20
print(resultado6)

#Si solamente una de las condiciones del mayor/menor o igual que... se cumple el resultado sera verdadero

