#operadores aritmeticos
num1 = 10
num2 = 3

#suma
resultado1 = num1 + num2
print (resultado1)

#resta
resultado2 = num1 - num2
print(resultado2)

#multiplicacion
resultado3 = num1 * num2
print(resultado3)

#division
resultado4 = num1 / num2
print(resultado4)

#division redondeada
resultado5 = num1 // num2
print(resultado5)

#modulo devuleve el residuo 
resultado6 = num1 % num2
print(resultado6)

#exponencial
resultado7 = num1 ** num2
print(resultado7)

#Todos los operadores aritmeticos siguen el orden de operaciones de las matematicas ejemplo de:     
resultado8 = 3**3 * (13/5 - (2*4))
print(resultado8)